public class Students {
    public int id;
    public String name;
    public String surname;
    public String group;
    public  int gpa;

    public Students(int id, String name, String surname, String group, int gpa) {
        this.id = id;
        this.name = name;
        this.surname = surname;
        this.group = group;
        this.gpa = gpa;
    }
}
